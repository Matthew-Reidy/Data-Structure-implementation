public class productObj {
    //object boiler plate
    public String product;
    public double price;

    public void setProduct(String product){
        this.product = product;
    }
    public String getProduct(){
        return this.product;
    }
    public void setPrice(double price){
        this.price = price;
    }
    public double getPrice(){
        return this.price;
    }
}
